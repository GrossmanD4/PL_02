def mas(n1,n2):
    if n1==n2:
        print("Los dos números son iguales.")
    else:
        print("El máximo de los dos es ",max(n1,n2),".")

def mis(n1,n2):
    if n1==n2:
        print("Los dos números son iguales.")
    else:
        print("El mínimo de los dos es ",min(n1,n2),".")


